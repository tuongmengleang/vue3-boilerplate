<script setup lang="ts">
/* IMPORTS */
import { reactive, computed } from 'vue'

/* PROPS */
const props = defineProps({
    color: {
        type: String,
        default: 'primary'
    },
    to: {
        type: [ String, Object ],
        default: undefined
    },
    href: {
        type: String,
        default: undefined
    }
})

/* STATES */
const defaultStyle = `
      box-border relative z-30 inline-flex items-center justify-center
      w-auto px-8 py-3 overflow-hidden font-bold text-white transition-all
      duration-300 rounded-md cursor-pointer group ring-offset-2 ring-1 ease
      focus:outline-none
    `
const styles = reactive<{ [key: string]: string }>({
    none: '',
    primary:
        'bg-indigo-600 ring-indigo-300 ring-offset-indigo-200 hover:ring-offset-indigo-500',
    secondary:
        'bg-teal-600 ring-teal-300 ring-offset-teal-200 hover:ring-offset-teal-500'
})

const selectedStyle = computed(() =>
    props.color in styles ? styles[props.color] : styles.primary
)
</script>

<template>
    <RouterLink
        v-if="to"
        :class="`${defaultStyle} ${selectedStyle}`"
        :to="to"
    >
        <span
            class="absolute bottom-0 right-0 w-8 h-20 -mb-8 -mr-5 transition-all duration-300 ease-out transform rotate-45 translate-x-1 bg-white opacity-10 group-hover:translate-x-0"
        />
        <span
            class="absolute top-0 left-0 w-20 h-8 -mt-1 -ml-12 transition-all duration-300 ease-out transform -rotate-45 -translate-x-1 bg-white opacity-10 group-hover:translate-x-0"
        />
        <span class="relative z-20 flex items-center text-sm gap-2">
            <slot name="prepend" />
            <slot />
            <slot name="append" />
        </span>
    </RouterLink>
    <a
        v-else
        :href="href"
        :class="`${defaultStyle} ${selectedStyle}`"
    >
        <span
            class="absolute bottom-0 right-0 w-8 h-20 -mb-8 -mr-5 transition-all duration-300 ease-out transform rotate-45 translate-x-1 bg-white opacity-10 group-hover:translate-x-0"
        />
        <span
            class="absolute top-0 left-0 w-20 h-8 -mt-1 -ml-12 transition-all duration-300 ease-out transform -rotate-45 -translate-x-1 bg-white opacity-10 group-hover:translate-x-0"
        />
        <span class="relative z-20 flex items-center text-sm gap-2">
            <slot name="prepend" />
            <slot />
            <slot name="append" />
        </span>
    </a>
</template>

